#include <cmath>
#include <iostream>
using namespace std;
int main() {
    int k,n;
    cout<<"Вычисление степени числа к с показателем н"<<endl;
    cout<<"Введите число k"<<endl;
    cin>>k;
    cout<<"Введите число n"<<endl;
    cin>>n;
    cout<<"Результат: "<< pow(k,n) <<endl;
    
    
    return 0;
}
